package com.ams.entity;

public class Field {
	private int fid;
	private String fname;
	private int f_numberlimit;
	private String f_state;
	public int getFid() {
		return fid;
	}
	public void setFid(int fid) {
		this.fid = fid;
	}
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public int getF_numberlimit() {
		return f_numberlimit;
	}
	public void setF_numberlimit(int f_numberlimit) {
		this.f_numberlimit = f_numberlimit;
	}
	public String getF_state() {
		return f_state;
	}
	public void setF_state(String f_state) {
		this.f_state = f_state;
	}
	

}
