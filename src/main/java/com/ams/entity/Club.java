package com.ams.entity;

public class Club {
	private int cid;
	private String cname;
	private String club_contents;
	private String club_logo;
	private String club_state;
	
	private int club_num;
	private int club_act;
	private int club_uid;
	
	private int uid;
	
	private int ctid;
	private String club_type_name;
	
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getClub_contents() {
		return club_contents;
	}
	public void setClub_contents(String club_contents) {
		this.club_contents = club_contents;
	}
	public String getClub_logo() {
		return club_logo;
	}
	public void setClub_logo(String club_logo) {
		this.club_logo = club_logo;
	}
	public String getClub_state() {
		return club_state;
	}
	public void setClub_state(String club_state) {
		this.club_state = club_state;
	}
	public int getClub_num() {
		return club_num;
	}
	public void setClub_num(int club_num) {
		this.club_num = club_num;
	}
	public int getClub_act() {
		return club_act;
	}
	public void setClub_act(int club_act) {
		this.club_act = club_act;
	}
	public int getClub_uid() {
		return club_uid;
	}
	public void setClub_uid(int club_uid) {
		this.club_uid = club_uid;
	}
	public int getCtid() {
		return ctid;
	}
	public void setCtid(int ctid) {
		this.ctid = ctid;
	}
	public String getClub_type_name() {
		return club_type_name;
	}
	public void setClub_type_name(String club_type_name) {
		this.club_type_name = club_type_name;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	
	

}
