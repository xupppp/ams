package com.ams.entity;

public class ClubType {
	private int ctid;
	private String club_type_name;
	private String club_type_state;
	public int getCtid() {
		return ctid;
	}
	public void setCtid(int ctid) {
		this.ctid = ctid;
	}
	public String getClub_type_name() {
		return club_type_name;
	}
	public void setClub_type_name(String club_type_name) {
		this.club_type_name = club_type_name;
	}
	public String getClub_type_state() {
		return club_type_state;
	}
	public void setClub_type_state(String club_type_state) {
		this.club_type_state = club_type_state;
	}
	

}
